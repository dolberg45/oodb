package model;

import annotations.*;

import java.util.List;

@Entity
public class Dealer {

    @Column
    private Long id;

    @Column
    private String name;


    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
